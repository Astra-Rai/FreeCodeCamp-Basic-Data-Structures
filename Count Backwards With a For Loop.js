//Count Backwards With a For Loop
//create an empty array as a placeholder for odd numbers pushed to myArray
var myArray = [];
//starting point = 9, if i is greater than 10, decrement by 2, this will provide all odd numbers
for(var i = 9; i > 0; i -=2){
  //use push method and push odd numbers to myArray
  myArray.push(i);
}
// Only change code below this line.
console.log(myArray);

